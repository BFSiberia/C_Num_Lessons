void PrintImg(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            //Console.Write($"{image[i, j]} ");
            if(image[i,j]==0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}

void FillImg(int row, int col)
{
    if(pic[row, col]==0)
    {
        pic[row,col] =1;
        FillImg(row-1,col);
        FillImg(row,col-1);
        FillImg(row+1,col);
        FillImg(row,col+1);
    }
}

