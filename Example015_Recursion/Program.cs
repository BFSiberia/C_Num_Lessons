double Fibbo(int n) //f(1) =1, f(2) =1, f(n) = f(n-1) + f(n-2) рекурсия фибоначчи
{
    if (n==1 || n == 2) return 1;
    else return Fibbo(n-1) + Fibbo(n-2);
}

